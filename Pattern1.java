public class Pattern1          // Println Vs Print
{
    public static void main(String[] args){
        System.out.println("*");
        System.out.println("*");  // next line println is used
        System.out.println("*");


        System.out.print("*");
        System.out.print("*");  // no next line print is used
        System.out.print("*");

    }
}