class Incrementation
{
    public static void main(String[] args) {
        int a=5;
        a++;  //a=a+1; iska matlab a ki existing value ko ek number se badana means 5 to 6 ho jayegi yaha (post increment)
        System.out.println(a);
        a--; //a=a-1; iska matlab a ki existing value ko ek number se kam karna means 6 to 5 ho jayegi yaha (post decrement)
        System.out.println(a);
    }
}